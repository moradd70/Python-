# Conditionals
x = 4

# Basic If statement
if x < 6:
    print('this is an if statement')
    
# If Else

if x < 6:
    print('this is true')
else:
    print('this is false')     
    
    
# Elif
color = 'red'

if color == 'red':
    print('Color is red')  
elif color == 'blue':
    print('Color is blue')  
else:
    print('Color is neither Red or Blue')    
    
# Nested if
if color == 'red':
    if x < 10:
        print('Color is and x is less than 10')
        
# Logical Operators
if color == 'red' and x < 10:
    print('Color is and x is less than 10')


    