# String Functions

myStr = 'Hello World'

# Capitalize
print(myStr.capitalize())

# Swap Case
print(myStr.swapcase())

# Get Length
print(len(myStr))

# Replace
print(myStr.replace('World', 'Everyone'))

sub = "l"
print(myStr.count(sub))

# Start with
print(myStr.startswith('H'))

# End with
print(myStr.endswith('d'))

# Split to list
print(myStr.split())

# find
print(myStr.find('World'))

# index
print(myStr.index('World'))

# is all alphanumeric
print(myStr.isalnum())

# is all alphabetic
print(myStr.isalpha())

# is numeric
print(myStr.isnumeric())